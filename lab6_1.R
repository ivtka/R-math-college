x <- c(0.45, 0.46, 0.47, 0.48, 0.49, 0.50, 
       0.51, 0.52, 0.53, 0.54, 0.55, 0.56)
y <- c(20.19, 19.61, 18.94, 18.17, 17.30, 
       16.31, 15.20, 13.95, 12.55, 10.99, 9.26, 7.35)
n <- 12

derivetive <- function(x, y, n) {
  df <- c(1:n)
  h <- x[2] - x[1]
  for (i in 1:n) {
    if (i == 1) {
      df[i] <- (y[2] - y[1]) / h
    } else {
      if (i == n) {
        df[i] <- (y[n] - y[n-1]) / h
      } else {
        df[i] <- (y[i+1] - y[i-1]) / (2*h)
      }
    }
  }
  return(df)
}

df <- derivetive(x, y, n)